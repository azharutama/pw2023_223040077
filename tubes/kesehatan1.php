                        <h5 class="card-title">Plt Bupati Bogor Harap RSUD Leuwiliang Beri Pelayanan Kesehatan Optimal pada Masyarakat</h5>
                        <p class="card-text">PORTALJABAR, KAB. BOGOR - Plt. Bupati Bogor Iwan Setiawan menyatakan adanya penilaian akreditasi terhadap RSUD Leuwiliang dapat menjadikan layanan kesehatan di RSUD Leuwiliang semakin optimal kepada masyarakat.

                            Hal itu dijelaskan Iwan saat menghadiri kegiatan penilaian akreditasi Rumah Sakit RSUD Leuwiliang oleh tim Lembaga Akreditasi RS Damar Husada Paripurna (LARS-DHP), di RSUD Leuwiliang, Kabupaten Bogor, Rabu (15/3/2023).

                            "Selamat datang di RSUD Leuwiliang yang merupakan RS kebanggan Kabupaten Bogor, ini mungkin akreditasi yang terakhir di RSUD Leuwiliang. Karena sebelumnya sudah dilakukan di tiga RSUD yakni RSUD Ciawi, RSUD Cibinong dan RSUD Cileungsi" katanya.

                            Menurutnya, dengan adanya penilaian akreditasi tersebut dapat berdampak positif terhadap kualitas pelayanan bagi masyarakat Kabupaten Bogor mulai dari sarana prasarana, tenaga medis dokter dan perawat serta yang lainnya, terlebih dengan tagline RSUD Leuwiliang "Paripurna", 

                            "Saya sebagai kepala daerah sangat mensupport bagaimana akreditasi di empat RSUD kebanggaan Kabupaten Bogor ini punya nilai yang sama yakni paripurna serta jadi kebanggaan sesuai harapan masyarakat dan kita semua," ucapnya.

                            Ia yakin adanya RSUD Leuwiliang ini bisa memberikan layanan yang terbaik buat masyarakat, terutama masyarakat di wilayah Bogor Barat tidak hanya Leuwiliang tapi juga Jasinga, Tenjo, Cigudeg dan Sukajaya semua kita layani bersama.

                            "Mudah-mudahan ke depan RSUD Leuwiliang menjadi rumah sakit rujukan, semoga hari ini hasilnya baik. Serta kedepan RSUD ini tipe nya nanti naik, karena saat ini masih tipe B," ujarnya.

                            Ketua Tim Surveyor Andriani Tri Susilowati mengungkapkan, bahwa penilaian akreditasi ini akan dilakukan selama tiga hari pada 10, 15 dan 16 Maret 2023.

                            "Survey sudah kami lakukan sejak tanggal 10 Maret kemarin secara daring, alhamdulillah berjalan dengan lancar. Hari ini survey kami lakukan dengan luring selama dua hari yaitu tanggal 15 dan 16 Maret. Setelah ini kami akan mengirimkan hasil ke LARS-DHP kemudian nanti akan ditelaah dan LARS-DHP yang akan memutuskan status kelulusannya," ucapnya. 
