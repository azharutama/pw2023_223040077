                        <h5 class="card-title">Diskop UKM Hadirkan Garut Packaging House sebagai Layanan Publik bagi UMKM</h5>
                        <p class="card-text">PORTALJABAR, KAB. GARUT - Keberadaan kemasan dalam sebuah brand atau produk tentunya menjadi salah satu hal yang harus diperhatikan, karena kemasan yang bagus biasanya akan banyak menarik minat dari konsumen.

                          Tentunya, kemasan yang berkualitas itu tidak hanya berlaku untuk pelaku industri saja, pelaku Usaha Mikro Kecil Menengah (UMKM) pun kini harus berlomba-lomba membuat packaging yang menarik, guna mendatangkan minat membeli daripada konsumen.

                          Hal tersebut nampaknya disadari betul oleh Pemerintah Daerah Kabupaten (Pemdakab) Garut melalui Dinas Koperasi dan UKM (Diskop UKM) Kabupaten Garut, dengan menghadirkan layanan publik gratis bagi pelaku usaha yaitu Garut Packaging House (GPH) yang berlokasi di gedung lama Diskop UKM Garut.

                          Kepala Bidang (Kabid) Pengembangan Usaha Koperasi dan Usaha Mikro Diskop UKM Garut Yadi Arriyadi menyampaikan, jika GPH ini lahir dari inovasi aksi perubahan diklat kepemimpinan dari Sekretaris Diskop UKM Garut Erni Herdiani, yang sebelumnya menjabat sebagai Kabid Pengembangan Usaha Koperasi dan Usaha Mikro Diskop UKM Garut.

                          "Kemudian Garut Packaging House atau kita singkat dengan GPH itu merupakan layanan gratis yang dipersembahkan bagi pelaku usaha mikro di Kabupaten Garut, dengan tujuan untuk membantu meningkatkan daya saing produk usaha mikro, dari segi kemasan dan juga label pada kemasan, sehingga diharapkan dapat memberikan nilai tambah yang lebih, ketika nantinya masuk ke pasar akan mendapatkan perhatian dari konsumen untuk dibeli tentunya," kata Yadi ketika diwawancara, Jum'at (28/4/2023).

                          Ia memaparkan ada beberapa benefit yang didapat oleh pelaku usaha ketika memanfaatkan layanan GPH, seperti mendapatkan pembuatan desain kemasan secara gratis, mendapatkan 20 standing pouch full print ukuran 14x20 centimeter (cm) secara gratis, hingga 3 kali revisi untuk desain kemasan secara gratis.

                          Bagi pelaku usaha yang berminat mendapatkan layanan dari GPH ini, lanjut Yadi, bisa mendaftarkan diri melalui link pendaftaran yang telah disediakan oleh pihaknya dan tercantum di instagram resmi milik Diskop UKM Garut.

                          "Jadi pelaku usaha mikro bisa daftar dulu secara online, karena mohon maaf juga mungkin karena keterbatasan pelayanan dari kami, ada istilahnya semacam daftar tunggu atau waiting list, tapi jangan khawatir ketika Bapak Ibu pelaku usaha mikro ini udah mendaftar, operator kami ini teman-teman ini akan diverifikasi datanya," ucapnya.

                          Ia memaparkan sebelum beranjak ke proses desain kemasan hingga pencetakan kemasan, para pelaku usaha yang mendaftarkan diri di layanan GPH ini, akan mendapatkan edukasi terlebih dahulu dari pihaknya, salah satunya terkait dengan hal-hal apa yang wajib ditampilkan dalam kemasan.

                          "Oh baik untuk persyaratan kami rasa tidak berat dan mungkin itu sangat mudah, tapi kami tentu memerlukan sesuatu yang menjamin bahwa yang datang ke sini itu adalah benar-benar pelaku usaha mikro, dan yang kami maksudkan itu adalah NIB atau nomor induk berusaha," ujarnya.

                          Berdasarkan laporan dari operator GPH, lanjut Yadi, dalam satu bulan terakhir pihaknya melayani sekitar 20 pelaku usaha mikro untuk dibuatkan desain kemasan dan pencetakan kemasannya. Ia berharap, jika diasumsikan dalam 1 bulan ada 20 pelaku usaha yang dilayani, maka dalam 1 tahun pihaknya bisa melayani kurang lebih 240 pelaku usaha.

                          Guna memperluas layanan packaging ini, Yadi mengatakan jika pihaknya berencana akan melakukan kegiatan semacam design on the spot, agar mampu menjangkau para pelaku usaha yang berada jauh dengan lokasi GPH ini.

                          "Jadi kami akan mendatangi desa-desa tertentu yang mungkin pelaku usahanya memiliki hambatan akses ke tempat GPH ini, biar nanti kami datang ke sana kumpul di suatu tempat, apakah di balai desa atau di tempat usahanya, beberapa orang kumpul, kita akan buatkan desain kemasan secara free juga sama seperti itu," katanya.

                          Ia berharap adanya GPH ini bisa menjadi motivasi bagi pihaknya untuk memberikan pelayanan publik yang lebih berkualitas bagi pelaku usaha mikro, dan juga diharapkan manfaatnya bisa benar-benar dirasakan langsung oleh masyarakat khususnya pelaku usaha mikro. </p>
