                        <p class="card-text">PORTALJABAR, KOTA BANDUNG - Hari Buruh Internasional atau juga dikenal sebagai May Day diperingati setiap 1 Mei untuk mengenang kontribusi para pekerja di seluruh dunia. Hari ini juga menandai dimulainya perjuangan untuk hak-hak pekerja, seperti hak untuk memperoleh jam kerja yang layak. 

                        Masih ingat dengan peristiwa kerusuhan Haymarket di Chicago tahun 1886 silam? Kala itu, para pekerja menggelar aksi untuk menyampaikan tuntutan mereka kepada pemerintah atas jam kerja dan upah yang tidak layak. Mereka harus bekerja selama 12 jam/hari atau 60 jam/minggunya dengan upah yang tidak sebanding.

                        Apa yang mereka usahakan di masa lalu tak sia-sia. Keadilan mulai ditegakkan, jam kerja di beberapa negara telah mengalami perubahan. Pada tahun 2021 tercatat, jam kerja di Amerika Serikat berubah menjadi 6,75 jam/hari, Selandia Baru 6,73 jam/hari, dan Jepang 6,68 jam/hari. Kebijakan jam kerja di Jepang juga termasuk mengharuskan liburan musiman, yang menjamin pekerja dapat menghabiskan waktu bersama keluarga mereka.

                        Tak hanya di negara-negara tersebut, Indonesia pun mulai memberlakukan kebijakan terkait jam kerja yang tertuang dalam PP Nomor 35 Tahun 2021 Pasal 77 yang berisi: Bekerja 6 hari dalam seminggu dengan waktu kerja 7 jam dalam sehari atau 40 jam dalam seminggu dan bekerja 5 hari dalam seminggu dengan waktu kerja 8 jam dalam sehari atau 40 jam dalam seminggu.

                        Jabar berhasil mengimplementasikan kebijakan terkait jam kerja tersebut dengan angka di bawah 40 jam/minggu.

                        “Per tahun 2020–2021, rata-rata buruh di Jabar bekerja selama 33,3 jam/minggu. Sebanyak 15,39 juta orang masih lebih banyak yang bekerja selama 35 jam/minggu. Namun, ada juga 650 ribu orang yang hanya bekerja 1–7 jam/minggu,” ujar Kepala Diskominfo Provinsi Jabar Ika Mardiah.

                        Lebih lanjut, per tahun 2021, 69,41 persen dari penduduk yang bekerja di Jabar adalah pekerja penuh (minimal 35 jam/minggu), 21,79 persen adalah pekerja paruh waktu, dan 8,8 persen adalah setengah penganggur.

                        Hak-hak pekerja telah berkembang dengan baik. Mereka kini berhak mendapatkan waktu kerja dan istirahat yang layak. 

                      Hari Buruh Internasional menjadi hari untuk menghargai para pekerja dan pengorbanan mereka untuk memastikan hak-hak pekerja terpenuhi. Hari ini juga mengingatkan kita untuk tetap berjuang untuk hak-hak pekerja di seluruh dunia.</p>
