          <p class="card-text">PORTALJABAR, KAB. GARUT - Pemerintah Daerah Kabupaten (Pemdakab) Garut menggelar High Level Meeting Tim Pengendalian Inflasi Daerah Kabupaten Garut Tahun 2023 yang dilaksanakan di Ballroom Hotel Harmoni, Kabupaten Garut, Selasa (21/3/2023).

            Asisten Daerah II Bidang Perekonomian dan Pembangunan Teti Sarifeni menyampaikan, kondisi inflasi di Kabupaten Garut pada bulan Maret ini tidak mengalami kenaikan harga, meskipun di bulan sebelumnya sempat mengalami kenaikan, namun kini sudah normal kembali.

            Teti menerangkan, pada high level meeting kali ini disepakati tujuh kesepakatan, di mana salah satunya yaitu melakukan monitoring harga ketersediaan pangan secara rutin selama Hari Besar Keagamaan Nasional (HBKN), dimulai pada hari Rabu besok, yang dipimpin oleh Sekretaris Daerah (Sekda) Garut Nurdin Yana.

            "Kemudian yang kedua mengoptimalkan dan memantau pelaksanaan beras SPHP (Stabilisasi Pasokan dan Harga Pangan) dan minyak kita di pasar sesuai dengan HET (Harga Eceran Tertinggi)," katanya.

            Kesepakatan selanjutnya, lanjut Teti, adalah beras SPHP dan minyak goreng kita tidak hanya didistribusikan ke pasar tradisional tapi juga ke retail modern. Selain itu, pihaknya juga akan mendorong realisasi Kerja sama Antar Daerah (KAD) yang sudah ada untuk memastikan ketersediaan pasokan.

            "Lima mengoptimalkan pengisian data 20 komoditas penyumbang inflasi di Si Linda Jabar (Sistem Informasi Pengendalian Inflasi Daerah di Jawa Barat) sebagai dasar pemantauan harga selama HBKN. Namun sebelum ke si Linda, kita akan sepakat untuk masuk dulu ke sistem pangan, sispangan yang ada di Sekretariat Daerah," ucapnya.

            Kemudian, lanjut Teti, pihaknya akan mengkomunikasikan ketersediaan pasokan pangan agar masih dalam kondisi yang aman untuk menjaga ekspektasi masyarakat. Dan Teti menyampaikan bahwa kesepakatan yang terakhir adalah menyikapi kenaikan HET LPG 3Kg.

            "Maka perlu dilakukan monitoring atau pengawasan terhadap pendistribusian LPG 3kg sesuai kebijakan sejak 1 maret 2023. Minimal 80% penjualan ke pengguna akhir dan 20% ke pengecer," ujarnya.

            Teti menerangkan, saat ini pasokan pangan di Kabupaten Garut terutama menjelang Ramadan sampai dengan Idulfitri adalah surplus, jadi dapat dipastikan bahwa kondisi pasokan pangan sampai hari lebaran nanti di Kabupaten Garut terbilang aman. </p>
