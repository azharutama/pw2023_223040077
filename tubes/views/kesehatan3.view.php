            PORTALJABAR, KOTA BANDUNG - Sekretaris Daerah Provinsi Jawa Barat Setiawan Wangsaatmaja menghadiri Silaturahmi Keluarga Besar Dinas Pemuda dan Olahraga Provinsi Jabar yang dirangkaikan dengan Peluncuran Brand Ambassador Mantan Atlet Nasional dan Internasional asal Jawa Barat di Kantor Dinas Pemuda dan Olahraga Provinsi Jawa Barat, Kota Bandung, Selasa (2/5/23).

            Sekda Setiawan menuturkan, atlet mempunyai peranan yang sangat penting dalam memajukan olahraga suatu daerah, khususnya Jawa Barat.

            "Maka para atlet ini harus diapresiasi dengan baik sehingga masyarakat pun mengenal melalui prestasi yang diperoleh," ucapnya

            Menurut Setiawan, sebagian besar mantan atlet berprestasi baik tingkat nasional maupun internasional banyak menjadi ASN di Dinas Pemuda dan Olahraga Provinsi Jabar. Oleh karena itu atlet yang sekarang ada harus memiliki peran manajerial.

            “Jadi jangan sampai jadi ASN terus kemudian "so that's it" semuanya. Saya "nggak" mau lagi ditemukan di olahraga, jangan begitu. Saya pesan pada teman-teman atlet semuanya.,” ungkap Setiawan.

            “Manusia itu diciptakan otak kiri dan kanan. Kiri itu untuk numerik, untuk hal-hal berhitung dan lain sebagainya. Kalau otak kanan lebih kepada bagaimana "taste", seni, dan strategi kita,” imbuhnya.

            Menurut Setiawan, Dinas Pemuda dan Olahraga banyaknya bobot di otak kanan sehingga harus mampu dioptimalkan. Caranya dengan memberikan kesempatan untuk berkarya melalui ilmu yang telah diperoleh selama menjadi atlet dengan atlet lain di KONI, bahkan bisa menjadi "brand ambassador" salah satu dinas dalam menyosialisasikan programnya.

            Setiawan menuturkan, ketika atlet menjadi "brand ambassador" sebuah dinas, maka masyarakat pun akan mengetahui prestasi yang dimiliki para atlet tersebut dan Jawa Barat.

            "Makanya saya mengajak Kepala Dinas Kominfo, tolong profil-profil ini (atlet) diangkat lagi bahwa Jawa Barat punya ini lho ,” katanya.

            “Kemudian misalnya para kepala dinas yang punya program, silahkan pilih atlet yang tepat. Misalnya Pak Bambang dari Bina Marga sudah mengincar Kang Ade (atlet binaraga) karena fotonya bisa angkat stoomwals,” tutur Sekda Setiawan.

            Tentu saja tidak dipungkiri bahwa ke depannya para mantan atlet ini pun suatu saat mampu menjadi inspirasi bagi atlet lain dan generasi muda untuk berprestasi. Terlebih dalam mempersiapkan PON (Pekan Olahraga Nasional) pada 2024. Para atlet harus dikelola dengan baik dalam segi kesehatan maupun sarana prasaranya.

            “Mengapa moment ini saya ambil karena tahun 2024 menghadapi PON lagi. Saya "nggak" mau gagal. Walaupun Pak Gubernur dan saya sudah "nggak" ada semuanya, tapi saya ingin 2024 kita "hattrick" (juara umum),” ungkapnya.

            “Kalau tidak ada "awareness" seperti ini saya rasa berat,” pungkas Setiawan </p>
