                        <h5 class="card-title">Garut Targetkan Naik Peringkat Madya pada Penilaian Kabupaten Layak Anak Tahun 2023</h5>
                        <p class="card-text">PORTALJABAR, KAB. GARUT - Koordinator Klaster 3 untuk Kesehatan Dasar dan Kesejahteraan Forum Anak Daerah (FAD) Kabupaten Garut Hudzaifah Hidayat Putra mengungkapkan, sudah sangat banyak kegiatan dari FAD Kabupaten Garut yang berpengaruh terhadap penilaian Kabupaten Layak Anak (KLA), sehingga menorehkan prestasi membanggakan.

                            Hudzaifah berharap, ke depannya pemerintah daerah dapat lebih meningkatkan keterlibatan dan mendengar suara FAD Kabupaten Garut dalam setiap kegiatan, termasuk dalam hal perencanaan.

                            Hal tersebut dikatakan Hudzaifah pada Rapat Koordinasi Evaluasi dan Persiapan Penilaian KLA Tahun 2023 di Aula Dinas Pengendalian Penduduk, Keluarga Berencana, Pemberdayaan Perempuan, dan Perlindungan Anak (DPPKBPPPA) Kabupaten Garut, Senin (20/3/2023).

                            "Mungkin dikarenakan Forum Anak juga dinaungi oleh pemerintah ya, jadi menurut saya sudah banyak banget hal kegiatan-kegiatan yang tentunya berpengaruh terhadap Kabupaten Layak Anak yang Forum Anak itu ikut kerja sama," katanya.

                            Menanggapi hal itu, Kepala Bidang Pemerintahan dan Pembangunan Manusia (PPM) Badan Perencanaan Pembangunan Daerah (Bappeda) Garut Iman Purnama Ridho menuturkan, program unggulan pemerintah daerah dalam rangka meningkatkan penilaian KLA ini adalah dengan melibatkan FAD Kabupaten Garut, termasuk dalam proses perencanaan pembangunan Daerah.

                            Iman juga mengungkapkan, tidak semua kabupaten/kota bisa mendapatkan peringkat dalam penilaian KLA baik di Jawa Barat maupun di Indonesia.

                            "Garut masuk (peringkat madya) dan kemarin tahun 2022 kita sempat mundur lagi ke pratama, tapi Alhamdulilah sekarang dengan kesepakatan tadi bapak/ibu dari SKPD ingin mendorong lagi ke target peringkat madya," ucapnya.

                            Iman menambahkan, ada beberapa hal yang membuat Kabupaten Garut turun menjadi peringkat pratama, di antaranya adalah banyaknya pernikahan di bawah umur yang didominasi oleh kaum perempuan di Kabupaten Garut.

                            "Tadi dibahas termasuk tadi masalah perkawinan di bawah umur, tadi Kemenag sudah menyebutkan tahun 2022 itu ada 488 pasangan yang statusnya pernikahan di bawah umur, kebanyakan itu dominasinya di kaum perempuan," ujarnya.

                            Kepala Bidang Perlindungan Anak DPPKBPPPA Kabupaten Garut Budi Kusmawan mengatakan, penilaian Kabupaten Layak Anak ini merupakan sebuah agenda tahunan dari Kementerian Pemberdayaan Perempuan dan Perlindungan Anak (Kemen PPPA), di mana kabupaten/kota diinstruksikan untuk melakukan proses input data terkait dengan dua indikator yaitu pemenuhan hak anak dan perlindungan khusus anak.

                            "Indikator-indikator ini nanti akan dijadikan acuan untuk diberikan penilaian, apakah misalnya Kabupaten Garut ini sudah masuk menjadi kabupaten yang sudah layak anak, dan kabupaten layak anak ini ada beberapa indikator data yang harus diinput terkait dengan di kelembagaan, di cluster pemenuhan hak anak, dan perlindungan khusus anak," katanya.

                            Budi mengatakan, kegiatan rapat ini bertujuan untuk meminta data-data pendukung dari setiap Satuan Kerja Perangkat Daerah (SKPD) terkait, yang sudah memberikan pemenuhan hak maupun perlindungan khusus anak dalam kegiatan di SKPD-nya masing-masing.

                            Senada dengan Kepala Bidang PPM, Budi mengatakan, di tahun 2021 Kabupaten Garut mengikuti penilaian KLA dengan peringkat kategori madya. Namun, pada tahun 2022 sendiri Kabupaten Garut mengalami penurunan peringkat menjadi kategori pratama.

                            "Tapi di tahun 2022 kita turun lagi ke pratama karena ada beberapa kategori penilaian, ada pratama itu nilainya 400-500 berdasarkan nilai evaluasi hasil dari input data. Terus ada juga madya itu 500-600, ada nindya 600-700, ada juga utama 700-800, dan ada KLA itu penilaian tertingginya kabupaten layak anak," ucapnya.

                            Ia berharap, semua pihak khususnya SKPD terkait dapat memberikan dukungan dalam hal penilaian KLA di Kabupaten Garut, sehingga dapat mencapai target penilaian kategori madya kembali di tahun 2023.
